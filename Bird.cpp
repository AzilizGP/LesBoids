Bird::Bird(void)
{
	x = (((double) rand())/RAND_MAX)*800;         //gives  a random number between 0 and 800
	y = (((double) rand())/RAND_MAX)*800;
	vx = (((double) rand())/RAND_MAX);
	vy = (((double) rand())/RAND_MAX);
}

Bird::Bird( const Bird& bird )
{
	x = bird.x;
	y = bird.y;
	vx = bird.vx;
	vy = bird.vy;
}

